#include "libft.h"

void    ft_bzero(void *s, size_t n)
{
    if (!s)
        return ;
    char *s1;
    size_t i;

    i = 0;
    s1 = (char *)s;
    while(*(s1+i) && i < n)
        *(s1+i) = 0;
        i++;
}