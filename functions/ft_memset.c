#include "../lib/libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (s && i < n)
	{
		*(str + i) = c;
		i++;
	}
	return (str);
}
